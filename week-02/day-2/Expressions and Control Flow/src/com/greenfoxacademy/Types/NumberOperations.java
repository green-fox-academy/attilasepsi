package com.greenfoxacademy.Types;

public class NumberOperations {
    public static void main(String[] args) {
        // Aritmetikai műveletek
        System.out.println(1 + 1); // Terminálban látható: 2
        System.out.println(5 - 2); // Terminálban látható: 3
        System.out.println(3 * 4); // Terminálban látható: 12
        System.out.println(6 / 2); // Terminálban látható: 3
        System.out.println(5 / 2); // Terminálban látható: 2
        System.out.println(5. / 2); // Terminálban látható: 2.5
        System.out.println(5 / 2.); // Terminálban látható: 2.5
        System.out.println(5. / 2.); // Terminálban látható: 2.5
        System.out.println(5 % 2); // Terminálban látható: 1
    }
}
