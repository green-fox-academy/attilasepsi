package com.greenfoxacademy.Types;

public class Floats {
    public static void main(String[] args) {
        // Pozitív, negatív és nulla (zero, not null)
        System.out.println(3.1415); // Terminálban látható: 3.1415
        System.out.println(-1.5); // Terminálban látható: -1.5
        System.out.println(0.0); // Terminálban látható: 0.0

        // A pont előtti és utáni 0-át nem szükséges kiírni
        System.out.println(.5); // Terminálban látható: 0.5
        System.out.println(5.); // Terminálban látható: 5.0
    }
}
