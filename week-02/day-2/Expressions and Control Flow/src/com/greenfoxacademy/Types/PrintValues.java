package com.greenfoxacademy.Types;

public class PrintValues {
    public static void main(String[] args) {
        // Egy karakterláncot (string) printel a terminal ablakba.
        System.out.println("Hello, World!");

        // Egy egész számot (integer) printel a terminal ablakba.
        System.out.println(42);

        // Egy racionális számot (avagy törtet, angolul floating point number)
        // printel a terminal ablakba.
        System.out.println(3.141592);
    }
}
